class Perro {

    // Setters
    set setNombre(nombre) { this.nombre = nombre; }
    set setRaza(raza) { this.raza = raza; }
    set setColor(color) { this.color = color; }
    set setEdad(edad) { this.edad = edad; }

    // Getters
    get getNombre() { return this.nombre; }
    get getEdad() { return this.edad; }
    get getRaza() { return this.raza; }
    get getColor() { return this.color; }

}